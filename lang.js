function translateDOM(node, lang) {
    if (node.nodeType === 3) {
        let text = node.nodeValue.trim();
        if (text) {
            // Restore original text if navigating back to EN, or just translating to KN
            // We use parent's data-en attribute
            let parent = node.parentElement;
            if (parent) {
                let orig = parent.getAttribute('data-en');
                if (!orig) {
                    // Only save if it's the only child to avoid complex mixed text node issues
                    if (parent.childNodes.length === 1 || parent.innerHTML === text) {
                        parent.setAttribute('data-en', text);
                        orig = text;
                    }
                }
                
                let targetText = orig || text;
                
                if (lang === 'kn' && globalDictionary['kn'][targetText]) {
                    node.nodeValue = globalDictionary['kn'][targetText];
                    if (targetText === 'KLPS Pangire (A)') {
                        parent.classList.remove('text-headline-md', 'md:text-display-lg-mobile');
                        parent.classList.add('text-lg', 'md:text-xl');
                    }
                } else if (lang === 'en' && orig) {
                    node.nodeValue = orig;
                    if (targetText === 'KLPS Pangire (A)') {
                        parent.classList.add('text-headline-md', 'md:text-display-lg-mobile');
                        parent.classList.remove('text-lg', 'md:text-xl');
                    }
                }
            }
        }
    } else if (node.nodeType === 1 && node.nodeName !== 'SCRIPT' && node.nodeName !== 'STYLE') {
        for (let i = 0; i < node.childNodes.length; i++) {
            translateDOM(node.childNodes[i], lang);
        }
        
        // Special case for buttons with icons (mixed content)
        if (node.classList && node.classList.contains('font-label-bold')) {
           // handled via child nodes mostly, but let's be careful
        }
    }
}

// Global Toggle
window.toggleLanguage = function() {
    let currentLang = localStorage.getItem('klps_lang') || 'en';
    let newLang = currentLang === 'en' ? 'kn' : 'en';
    localStorage.setItem('klps_lang', newLang);
    applyGlobalTranslations(newLang);
};

function applyGlobalTranslations(lang) {
    // Translate the toggle button text if it exists
    let toggleText = document.getElementById('lang-text');
    if (toggleText) {
        toggleText.innerText = lang === 'en' ? 'EN' : 'ಕನ್ನಡ';
    }
    
    // Translate the entire body
    translateDOM(document.body, lang);
    
    // Hack to fix mixed content buttons
    document.querySelectorAll('[data-en]').forEach(el => {
        let orig = el.getAttribute('data-en');
        if (lang === 'kn' && globalDictionary['kn'][orig]) {
            if(el.childNodes.length === 1 && el.childNodes[0].nodeType === 3) {
                el.innerText = globalDictionary['kn'][orig];
            }
        } else if (lang === 'en') {
             if(el.childNodes.length === 1 && el.childNodes[0].nodeType === 3) {
                el.innerText = orig;
             }
        }
    });
}

window.addEventListener('DOMContentLoaded', () => {
    let lang = localStorage.getItem('klps_lang') || 'en';
    setTimeout(() => applyGlobalTranslations(lang), 100);
});
